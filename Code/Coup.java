
public class Coup{
	
	private Piece pieceAvantD,pieceApresD;


//Constructeur

	public Coup(Piece piece1, Piece piece2){
		this.pieceAvantD=piece1;
		this.pieceApresD=piece2;
	}
	
	public Piece getPieceAvantD() {
		return pieceAvantD;
	}


	public void setPieceAvantD(Piece pieceAvantD) {
		this.pieceAvantD = pieceAvantD;
	}


	public Piece getPieceApresD() {
		return pieceApresD;
	}


	public void setPieceApresD(Piece pieceApresD) {
		this.pieceApresD = pieceApresD;
	}


	
	
	

}
		
		
		
		
			
			
		
		
		
	


