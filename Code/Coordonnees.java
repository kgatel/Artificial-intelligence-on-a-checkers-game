
public class Coordonnees {

	private int x,y;
	
	public Coordonnees() {
		x=-1;
		y=-1;
	}
	
	public Coordonnees(int x, int y) {
		this.x=x;
		this.y=y;
	}

	public int getX() {
		return x;
	}
		
	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}
	
	
}
